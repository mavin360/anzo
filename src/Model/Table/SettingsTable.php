<?php
namespace App\Model\Table;

use Cake\ORM\Query;
use Cake\ORM\RulesChecker;
use Cake\ORM\Table;
use Cake\Validation\Validator;

/**
 * Settings Model
 *
 * @method \App\Model\Entity\Setting get($primaryKey, $options = [])
 * @method \App\Model\Entity\Setting newEntity($data = null, array $options = [])
 * @method \App\Model\Entity\Setting[] newEntities(array $data, array $options = [])
 * @method \App\Model\Entity\Setting|bool save(\Cake\Datasource\EntityInterface $entity, $options = [])
 * @method \App\Model\Entity\Setting patchEntity(\Cake\Datasource\EntityInterface $entity, array $data, array $options = [])
 * @method \App\Model\Entity\Setting[] patchEntities($entities, array $data, array $options = [])
 * @method \App\Model\Entity\Setting findOrCreate($search, callable $callback = null, $options = [])
 */
class SettingsTable extends Table
{

    /**
     * Initialize method
     *
     * @param array $config The configuration for the Table.
     * @return void
     */
    public function initialize(array $config)
    {
        parent::initialize($config);

        $this->setTable('settings');
        $this->setDisplayField('id');
        $this->setPrimaryKey('id');
    }

    /**
     * Default validation rules.
     *
     * @param \Cake\Validation\Validator $validator Validator instance.
     * @return \Cake\Validation\Validator
     */
    public function validationDefault(Validator $validator)
    {
        $validator
            ->integer('id')
            ->allowEmpty('id', 'create');

        $validator
            ->scalar('help_phone')
            ->allowEmpty('help_phone');

        $validator
            ->scalar('help_phone_1')
            ->allowEmpty('help_phone_1');

        $validator
            ->scalar('help_email')
            ->allowEmpty('help_email');

        $validator
            ->scalar('fb_url')
            ->allowEmpty('fb_url');

        $validator
            ->scalar('twitter_url')
            ->allowEmpty('twitter_url');

        $validator
            ->scalar('instagram_url')
            ->allowEmpty('instagram_url');

        $validator
            ->scalar('linkedin_url')
            ->allowEmpty('linkedin_url');

        $validator
            ->scalar('app_url')
            ->allowEmpty('app_url');

       

        return $validator;
    }
}
